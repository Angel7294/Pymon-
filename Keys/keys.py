import pygame as pg
import os

class Keys:
    class Attack_Keys:
        a = pg.K_a
        s = pg.K_s
        d = pg.K_d
        f = pg.K_f

    class Item_Key:
        c = pg.K_c
        b = pg.K_b
        q = pg.K_q
        w = pg.K_w
        e = pg.K_e
        r = pg.K_r

    class Menu_Key:
        m = pg.K_m

    class Direction_Keys:
        up = pg.K_UP
        down = pg.K_DOWN
        left = pg.K_LEFT
        right = pg.K_RIGHT

